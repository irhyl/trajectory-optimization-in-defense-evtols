"""
eval_singletask.py
==================
Single-task evaluation of T1 (risk) and T2 (energy) with the fixed,
leak-free feature sets across all 16 Indian geographic regions.
T3 and T4 are reported as open challenges.

For the 10 new regions, paths point to the _10x.csv files generated by
expand_dataset_10x.py.  Original 6 regions use the baseline CSVs so that
evaluation is comparable to the published 6-region results.

Outputs summary_singletask.csv to outputs/ml/nn_results/
"""
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(REPO_ROOT / 'src'))

from evtol.ml.neural_network import (
    MultiTaskNN, StandardScalerNP, DenseLayer, DropoutLayer,
    auc_roc_numpy, f1_numpy, r2_numpy, mae_numpy, rmse_numpy,
    kfold_indices, compute_pos_weight,
)
import numpy as np
import pandas as pd
import csv

DS = REPO_ROOT / 'datasets'

def _plan(region: str, original_csv: str, tenx_csv: str):
    """Return best available planning CSV path for a region."""
    p10 = DS / tenx_csv
    p0  = DS / original_csv
    return p10 if p10.exists() else p0

def _veh(region: str):
    tenx = DS / region / 'vehicle' / 'vehicle_dataset_10x.csv'
    base = DS / region / 'vehicle' / 'vehicle_dataset.csv'
    return tenx if tenx.exists() else base

def _ctrl(region: str):
    tenx = DS / region / 'control' / 'control_dataset_10x.csv'
    base = DS / region / 'control' / 'control_dataset.csv'
    return tenx if tenx.exists() else base

REGIONS = {
    # Original 6 — prefer 10x CSV when available, fall back to baseline
    'delhi':         (_plan('delhi',     'delhi/planning_dataset/planning_dataset_10k.csv',
                                         'delhi/planning_dataset/planning_delhi_10x.csv'),
                      _veh('delhi'), _ctrl('delhi')),
    'mumbai':        (_plan('mumbai',    'mumbai/planning_dataset/planning_mumbai.csv',
                                         'mumbai/planning_dataset/planning_mumbai_10x.csv'),
                      _veh('mumbai'), _ctrl('mumbai')),
    'bangalore':     (_plan('bangalore', 'bangalore/planning_dataset/planning_bangalore.csv',
                                         'bangalore/planning_dataset/planning_bangalore_10x.csv'),
                      _veh('bangalore'), _ctrl('bangalore')),
    'arunachal':     (_plan('arunachal', 'arunachal/planning/planning_arunachal.csv',
                                         'arunachal/planning/planning_arunachal_10x.csv'),
                      _veh('arunachal'), _ctrl('arunachal')),
    'odisha':        (_plan('odisha',    'odisha/planning_dataset/planning_odisha.csv',
                                         'odisha/planning_dataset/planning_odisha_10x.csv'),
                      _veh('odisha'), _ctrl('odisha')),
    'ladakh':        (_plan('ladakh',    'ladakh/planning_dataset/planning_ladakh.csv',
                                         'ladakh/planning_dataset/planning_ladakh_10x.csv'),
                      _veh('ladakh'), _ctrl('ladakh')),
    # 10 new regions — generated by expand_dataset_10x.py
    'srinagar':      (DS/'srinagar/planning_dataset/planning_srinagar_10x.csv',
                      _veh('srinagar'), _ctrl('srinagar')),
    'chennai':       (DS/'chennai/planning_dataset/planning_chennai_10x.csv',
                      _veh('chennai'), _ctrl('chennai')),
    'kolkata':       (DS/'kolkata/planning_dataset/planning_kolkata_10x.csv',
                      _veh('kolkata'), _ctrl('kolkata')),
    'pune':          (DS/'pune/planning_dataset/planning_pune_10x.csv',
                      _veh('pune'), _ctrl('pune')),
    'jaisalmer':     (DS/'jaisalmer/planning_dataset/planning_jaisalmer_10x.csv',
                      _veh('jaisalmer'), _ctrl('jaisalmer')),
    'visakhapatnam': (DS/'visakhapatnam/planning_dataset/planning_visakhapatnam_10x.csv',
                      _veh('visakhapatnam'), _ctrl('visakhapatnam')),
    'guwahati':      (DS/'guwahati/planning_dataset/planning_guwahati_10x.csv',
                      _veh('guwahati'), _ctrl('guwahati')),
    'port_blair':    (DS/'port_blair/planning_dataset/planning_port_blair_10x.csv',
                      _veh('port_blair'), _ctrl('port_blair')),
    'jodhpur':       (DS/'jodhpur/planning_dataset/planning_jodhpur_10x.csv',
                      _veh('jodhpur'), _ctrl('jodhpur')),
    'imphal':        (DS/'imphal/planning_dataset/planning_imphal_10x.csv',
                      _veh('imphal'), _ctrl('imphal')),
}

# T1 features: planning-layer geometry + costs (no energy target, no threat target)
T1_FEATS_PLAN = [
    'path_length_m', 'time_cost_s', 'energy_cost_wh', 'threat_cost',
    'terrain_cost_mean', 'wind_cost_mean', 'obstacle_cost_mean',
    'fused_cost_mean', 'n_waypoints',
    'start_lat', 'start_lon', 'goal_lat', 'goal_lon',
    'start_alt_m', 'goal_alt_m',
]

# T2 features: planning geometry only (no energy_consumed_wh leak)
T2_FEATS_PLAN = [
    'path_length_m', 'time_cost_s', 'energy_cost_wh',
    'n_waypoints', 'start_alt_m', 'goal_alt_m',
    'terrain_cost_mean', 'wind_cost_mean', 'fused_cost_mean',
]
T2_FEATS_VEH = [
    'cruise_speed_ms', 'cruise_altitude_m', 'hover_time_s',
    'power_hover_elec_W', 'power_cruise_elec_W', 'figure_of_merit',
    'LD_ratio', 'propulsive_efficiency', 'hover_fraction', 'cruise_fraction',
    'pack_capacity_wh',
]


def make_single_task_model(d_in: int, task: str, pos_weight: float,
                           seed: int) -> MultiTaskNN:
    """Build a single-task model by zeroing weights of unused heads."""
    weights = {t: (1.0 if t == task else 0.0) for t in MultiTaskNN.TASKS}
    model = MultiTaskNN(d_in=d_in, task_weights=weights,
                        pos_weight=pos_weight, seed=seed)
    return model


def best_threshold_f1(y_true, y_score):
    best_f1, best_t = 0.0, 0.5
    for t in np.linspace(0.05, 0.95, 19):
        f = f1_numpy(y_true, y_score, threshold=t)
        if f > best_f1:
            best_f1, best_t = f, t
    return best_f1, best_t


all_results = {}

for region, (ppath, vpath, cpath) in REGIONS.items():
    print(f'\n=== {region} ===')
    if not Path(ppath).exists():
        print(f'  [skip] planning file not found: {ppath}')
        print(f'         Run: python scripts/expand_dataset_10x.py --region {region}')
        continue
    plan = pd.read_csv(ppath)
    veh  = pd.read_csv(vpath) if Path(vpath).exists() else pd.DataFrame()
    ctrl = pd.read_csv(cpath) if Path(cpath).exists() else pd.DataFrame()
    n = min(len(plan), len(veh), len(ctrl))
    plan, veh, ctrl = plan.iloc[:n], veh.iloc[:n], ctrl.iloc[:n]
    res = {'region': region, 'n': n}

    # ---------------------------------------------------------------
    # T1: Risk classification (planning features only)
    # ---------------------------------------------------------------
    t1_cols = []
    t1_names = []
    for f in T1_FEATS_PLAN:
        if f in plan.columns:
            t1_cols.append(pd.to_numeric(plan[f], errors='coerce').fillna(0.0).values)
            t1_names.append(f)
    X_t1 = np.column_stack(t1_cols).astype(np.float64) if t1_cols else None

    rl = next((df['risk_label'] for df in [plan, veh, ctrl]
               if 'risk_label' in df.columns), None)
    tgt_T1 = rl.values.astype(float) if rl is not None else None
    n_pos = int(np.nansum(tgt_T1)) if tgt_T1 is not None else 0
    pw = compute_pos_weight(tgt_T1, min_positives=10, max_weight=50.0) \
         if tgt_T1 is not None else 1.0
    print(f'  T1: {len(t1_names)} feats, pos={n_pos}/{n}, pw={pw:.2f}')
    res['T1_pos_rate'] = round(n_pos / n, 4)
    res['T1_pos_weight'] = round(pw, 2)

    if X_t1 is not None and tgt_T1 is not None and n_pos >= 10:
        splits = kfold_indices(n, k=3, seed=42)
        t1_aucs, t1_f1s_opt = [], []
        for fold_i, (tr, val) in enumerate(splits):
            sc = StandardScalerNP(); Xtr = sc.fit_transform(X_t1[tr])
            Xval = sc.transform(X_t1[val])
            ytr = tgt_T1[tr]; yval = tgt_T1[val]
            n_pos_v = int(yval.sum()); n_neg_v = len(yval) - n_pos_v
            if n_pos_v < 2 or n_neg_v < 2:
                continue
            tgt_dict = {t: (ytr if t == 'T1_risk' else None)
                        for t in MultiTaskNN.TASKS}
            model = make_single_task_model(Xtr.shape[1], 'T1_risk', pw,
                                           seed=42 + fold_i)
            model.fit(Xtr, tgt_dict, epochs=100, batch_size=256,
                      lr=1e-3, patience=15, val_fraction=0.0, verbose=False)
            p_hat = model.predict(Xval)['T1_risk']
            t1_aucs.append(auc_roc_numpy(yval, p_hat))
            f1_opt, _ = best_threshold_f1(yval, p_hat)
            t1_f1s_opt.append(f1_opt)
            print(f'    Fold {fold_i+1}: AUC={t1_aucs[-1]:.4f}  F1*={t1_f1s_opt[-1]:.4f}')
        if t1_aucs:
            res['T1_auc_mean'] = round(float(np.mean(t1_aucs)), 4)
            res['T1_auc_std']  = round(float(np.std(t1_aucs)),  4)
            res['T1_f1opt_mean'] = round(float(np.mean(t1_f1s_opt)), 4)
            res['T1_f1opt_std']  = round(float(np.std(t1_f1s_opt)),  4)
    else:
        reason = 'degenerate imbalance' if n_pos < 10 else 'missing data'
        print(f'  T1 skipped: {reason}')
        res['T1_auc_mean'] = float('nan')
        res['T1_f1opt_mean'] = float('nan')

    # ---------------------------------------------------------------
    # T2: Energy regression (planning + vehicle features, no energy leak)
    # ---------------------------------------------------------------
    t2_cols, t2_names = [], []
    for f in T2_FEATS_PLAN:
        if f in plan.columns:
            t2_cols.append(pd.to_numeric(plan[f], errors='coerce').fillna(0.0).values)
            t2_names.append(f)
    for f in T2_FEATS_VEH:
        if f in veh.columns:
            t2_cols.append(pd.to_numeric(veh[f], errors='coerce').fillna(0.0).values)
            t2_names.append(f)
    X_t2 = np.column_stack(t2_cols).astype(np.float64)
    tgt_T2 = veh['energy_consumed_wh'].values.astype(float) \
              if 'energy_consumed_wh' in veh.columns else None
    print(f'  T2: {len(t2_names)} feats')

    if tgt_T2 is not None:
        splits = kfold_indices(n, k=3, seed=42)
        t2_r2s, t2_maes = [], []
        for fold_i, (tr, val) in enumerate(splits):
            sc = StandardScalerNP(); Xtr = sc.fit_transform(X_t2[tr])
            Xval = sc.transform(X_t2[val])
            ytr = tgt_T2[tr]; yval = tgt_T2[val]
            tgt_dict = {t: (ytr if t == 'T2_energy' else None)
                        for t in MultiTaskNN.TASKS}
            model = make_single_task_model(Xtr.shape[1], 'T2_energy', 1.0,
                                           seed=42 + fold_i)
            model.fit(Xtr, tgt_dict, epochs=100, batch_size=256,
                      lr=1e-3, patience=15, val_fraction=0.0, verbose=False)
            p_hat = model.predict(Xval)['T2_energy']
            t2_r2s.append(r2_numpy(yval, p_hat))
            t2_maes.append(mae_numpy(yval, p_hat))
            print(f'    Fold {fold_i+1}: R²={t2_r2s[-1]:.4f}  MAE={t2_maes[-1]:.1f} Wh')
        res['T2_r2_mean']  = round(float(np.mean(t2_r2s)),  4)
        res['T2_r2_std']   = round(float(np.std(t2_r2s)),   4)
        res['T2_mae_mean'] = round(float(np.mean(t2_maes)), 1)
        res['T2_mae_std']  = round(float(np.std(t2_maes)),  1)

    all_results[region] = res

# ---------------------------------------------------------------
print('\n\n' + '=' * 72)
print('SINGLE-TASK RESULTS (leak-free, gradient-clipped Adam)')
print('=' * 72)
print(f'{"Region":12s} | {"T1 AUC":>8s} {"±":>5s} {"T1 F1*":>7s} | '
      f'{"T2 R²":>7s} {"±":>5s} {"T2 MAE (Wh)":>12s}')
print('-' * 72)
for region, res in all_results.items():
    t1_auc  = res.get('T1_auc_mean',   float('nan'))
    t1_std  = res.get('T1_auc_std',    float('nan'))
    t1_f1   = res.get('T1_f1opt_mean', float('nan'))
    t2_r2   = res.get('T2_r2_mean',    float('nan'))
    t2_std  = res.get('T2_r2_std',     float('nan'))
    t2_mae  = res.get('T2_mae_mean',   float('nan'))
    skipped = '' if res.get('T1_pos_rate', 1) > 0.005 else ' (T1 skipped: <10 pos)'
    print(f'{region:12s} | {t1_auc:>8.4f} {t1_std:>5.4f} {t1_f1:>7.4f} | '
          f'{t2_r2:>7.4f} {t2_std:>5.4f} {t2_mae:>12.1f}{skipped}')

print('\n* F1 at threshold tuned on each validation fold')

# Save CSV
out = REPO_ROOT / 'outputs' / 'ml' / 'nn_results' / 'summary_singletask.csv'
if all_results:
    rows = list(all_results.values())
    with open(out, 'w', newline='') as f:
        w = csv.DictWriter(f, fieldnames=rows[0].keys())
        w.writeheader(); w.writerows(rows)
print(f'Saved: {out}')
