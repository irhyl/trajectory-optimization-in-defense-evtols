# UNIFIED PERCEPTION-PLANNING INTERFACE
# =============================================================================

class PerceptionPlanningBridge:
    """
    Unified interface for planning layer to access perception data.
    
    Provides:
        1. Perception state queries
        2. Constraint generation
        3. Feasibility assessment
        4. Batch dataset operations
    """
    
    def __init__(self, dataset_root: Optional[Path] = None):
        self.loader = PerceptionDataLoader(dataset_root)
        self.mapper = PerceptionToPlanningMapper()
        self._current_dataset = None
        self._current_scale = None
        
        logger.info("Perception-Planning Bridge initialized")
    
    def load_dataset(self, scale: int) -> int:
        """
        Load perception dataset for planning use.

        Args:
            scale: Number of rows to load (any positive integer; use a key from
                   loader.available_scales for the full dataset).

        Returns:
            Number of states loaded
        """
        self._current_dataset = self.loader.load_scale(scale, format='csv')
        self._current_scale = scale
        
        logger.info(
            f"Loaded perception dataset: {scale} cycles, "
            f"{len(self._current_dataset)} states for planning"
        )
        
        return len(self._current_dataset)
    
    def get_state_and_constraints(
        self,
        index: int,
    ) -> Tuple[Dict[str, float], PlanningConstraintSet]:
        """
        Get perception state and planning constraints by index.
        
        Args:
            index: Index in current dataset
        
        Returns:
            (state_dict, constraints)
        """
        if self._current_dataset is None:
            raise RuntimeError("No dataset loaded. Call load_dataset() first.")
        
        if index < 0 or index >= len(self._current_dataset):
            raise IndexError(f"Index {index} out of range [0, {len(self._current_dataset)-1}]")
        
        perception = self._current_dataset[index]
        return self.mapper.map_state(perception)
    
    def get_feasible_states(
        self,
        min_feasibility: float = 0.7,
    ) -> List[Tuple[int, Dict[str, float], PlanningConstraintSet]]:
        """
        Get all states with feasibility above threshold.
        
        Args:
            min_feasibility: Minimum feasibility score [0,1]
        
        Returns:
            List of (index, state, constraints) for viable planning
        """
        if self._current_dataset is None:
            raise RuntimeError("No dataset loaded. Call load_dataset() first.")
        
        feasible = []
        
        for idx, perception in enumerate(self._current_dataset):
            feasibility = perception.feasibility_score * (1.0 - perception.fused_risk)
            
            if feasibility >= min_feasibility:
                state, constraints = self.mapper.map_state(perception)
                feasible.append((idx, state, constraints))
        
        logger.info(f"Found {len(feasible)} feasible states out of {len(self._current_dataset)}")
        return feasible
    
    def get_statistics(self) -> Dict[str, Any]:
        """Get statistics on loaded dataset."""
        if self._current_dataset is None:
            return {}
        
        states = self._current_dataset
        risks = np.array([s.fused_risk for s in states])
        feasibilities = np.array([s.feasibility_score for s in states])
        winds = np.array([s.wind_speed_mps for s in states])
        
        return {
            'scale': self._current_scale,
            'total_states': len(states),
            'risk_mean': float(np.mean(risks)),
            'risk_std': float(np.std(risks)),
            'risk_min': float(np.min(risks)),
            'risk_max': float(np.max(risks)),
            'feasibility_mean': float(np.mean(feasibilities)),
            'feasibility_min': float(np.min(feasibilities)),
            'wind_mean_mps': float(np.mean(winds)),
            'wind_max_mps': float(np.max(winds)),
        }


