"""
Perception-Planning Integration Layer

Bridges perception outputs to planning inputs:
  • Loads perception datasets from outputs/perception_datasets/
  • Maps perception state → planning constraints
  • Validates perception-planning data pipeline
  • Provides unified query interface for planners

Architecture:
    ┌────────────────────────────┐
    │ Perception Datasets        │
    │ (5M cycles, 7 features)    │
    └────────────┬───────────────┘
                 ↓
    ┌────────────────────────────┐
    │ Perception Data Loader     │
    │ (Memory-mapped, batched)   │
    └────────────┬───────────────┘
                 ↓
    ┌────────────────────────────┐
    │ State Mapper               │
    │ (Perception→Planning)      │
    └────────────┬───────────────┘
                 ↓
    ┌────────────────────────────┐
    │ Constraint Generator       │
    │ (Risk, Energy, Safety)     │
    └────────────┬───────────────┘
                 ↓
    ┌────────────────────────────┐
    │ Planning Interface         │
    │ (Unified query API)        │
    └────────────────────────────┘
"""

import logging
import json
import numpy as np
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any
from dataclasses import dataclass, asdict
from datetime import datetime
import pandas as pd

logger = logging.getLogger(__name__)


# =============================================================================
# DATA STRUCTURES
# =============================================================================

@dataclass
class PerceptionState:
    """Perception observation from dataset."""
    latitude: float
    longitude: float
    altitude_m: float
    
    # Risk & feasibility from perception
    terrain_risk: float           # 0-1: terrain hazard
    wind_risk: float              # 0-1: wind hazard
    obstacle_risk: float          # 0-1: collision hazard
    threat_risk: float            # 0-1: threat hazard
    fused_risk: float             # 0-1: combined risk
    feasibility_score: float      # 0-1: landing/navigation feasibility
    
    # Environmental conditions
    elevation_m: float
    wind_speed_mps: float
    wind_direction_deg: float
    temperature_c: float = 15.0   # Default ISA

    def to_dict(self) -> Dict:
        return asdict(self)
    
    def as_planning_constraints(self) -> Dict[str, float]:
        """Convert perception to planning constraint bounds."""
        return {
            'max_risk_tolerance': 1.0 - self.fused_risk,      # Inverse: lower risk → higher tolerance
            'max_energy_relative': 1.0 + (self.wind_speed_mps / 10.0),  # Scale with wind
            'min_clearance_m': 50.0 + (self.obstacle_risk * 100.0),     # Risk increases required clearance
            'threat_avoidance_priority': self.threat_risk,
            'landing_feasible': self.feasibility_score > 0.7,
        }


@dataclass
class PlanningConstraintSet:
    """Constraints for planning derived from perception."""
    risk_budget: float              # Max allowable risk [0,1]
    energy_budget: float            # Energy multiplier
    min_clearance_m: float          # Minimum obstacle clearance
    threat_priority: float          # Threat avoidance weight [0,1]
    environmental_headwind_mps: float
    environmental_crosswind_mps: float
    
    def to_dict(self) -> Dict:
        return asdict(self)


