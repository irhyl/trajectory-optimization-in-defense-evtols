class PerceptionDataLoader:
    """
    Load perception datasets from disk.

    Primary source: outputs/perception_dataset/perception_full_dataset.csv
    (1,057,714 rows, 28 columns — generated by scripts/generate_perception_dataset.py)

    Column mapping to PerceptionState:
        lat, lon, alt_m → latitude, longitude, altitude_m
        terrain_cost    → terrain_risk
        wind_cost       → wind_risk
        obstacle_cost   → obstacle_risk
        threat_cost     → threat_risk  (= combined_threat_prob)
        fused_cost      → fused_risk
        elev_m          → elevation_m
        wind_speed_mps  → wind_speed_mps
        wind_dir_deg    → wind_direction_deg
        terrain_clearance_m used to derive feasibility_score
    """

    # Column names in perception_full_dataset.csv
    _COL_MAP = {
        'lat':                    'latitude',
        'lon':                    'longitude',
        'alt_m':                  'altitude_m',
        'terrain_cost':           'terrain_risk',
        'wind_cost':              'wind_risk',
        'obstacle_cost':          'obstacle_risk',
        'combined_threat_prob':   'threat_risk',
        'fused_cost':             'fused_risk',
        'elev_m':                 'elevation_m',
        'wind_speed_mps':         'wind_speed_mps',
        'wind_dir_deg':           'wind_direction_deg',
    }

    def __init__(self, dataset_root: Optional[Path] = None):
        """
        Args:
            dataset_root: Directory containing perception_full_dataset.csv.
                          Defaults to project_root/outputs/perception_dataset/
        """
        if dataset_root is None:
            current_file = Path(__file__)
            project_root = current_file.parent.parent.parent.parent
            dataset_root = project_root / "outputs" / "perception_dataset"

        self.dataset_root = Path(dataset_root)
        self._full_csv = self.dataset_root / "perception_full_dataset.csv"

        # available_scales: {1057714: path} if full CSV exists, else {}
        self.available_scales: Dict[int, Path] = {}
        if self._full_csv.exists():
            # Count rows quickly (wc -l equivalent)
            with open(self._full_csv, 'r') as f:
                n_rows = sum(1 for _ in f) - 1  # subtract header
            self.available_scales[n_rows] = self._full_csv
            logger.info(f"Perception data loader: full CSV {n_rows:,} rows at {self._full_csv}")
        else:
            logger.warning(f"Perception dataset not found at {self._full_csv}")

    def load_scale(self, scale: int, format: str = 'csv') -> List['PerceptionState']:
        """
        Load up to `scale` rows from the perception dataset.

        Args:
            scale: Maximum number of rows to load (use the key from available_scales
                   to load all rows, or any smaller integer for a sample).
            format: ignored — always reads CSV.

        Returns:
            List of PerceptionState objects
        """
        if not self._full_csv.exists():
            raise ValueError(
                f"Perception CSV not found: {self._full_csv}. "
                "Run scripts/generate_perception_dataset.py first."
            )
        return self._load_csv(self._full_csv, max_rows=scale)
    
    def _load_jsonl(self, file_path: Path) -> List[PerceptionState]:
        """Load from JSONL format."""
        states = []
        
        with open(file_path, 'r') as f:
            for line_num, line in enumerate(f, 1):
                try:
                    data = json.loads(line.strip())
                    state = PerceptionState(
                        latitude=float(data['latitude']),
                        longitude=float(data['longitude']),
                        altitude_m=float(data['altitude_m']),
                        terrain_risk=float(data.get('terrain_risk', 0.0)),
                        wind_risk=float(data.get('wind_risk', 0.0)),
                        obstacle_risk=float(data.get('obstacle_risk', 0.0)),
                        threat_risk=float(data.get('threat_risk', 0.0)),
                        fused_risk=float(data.get('fused_risk', 0.0)),
                        feasibility_score=float(data.get('feasibility_score', 0.5)),
                        elevation_m=float(data.get('elevation_m', 0.0)),
                        wind_speed_mps=float(data.get('wind_speed_mps', 0.0)),
                        wind_direction_deg=float(data.get('wind_direction_deg', 0.0)),
                        temperature_c=float(data.get('temperature_c', 15.0)),
                    )
                    states.append(state)
                except (json.JSONDecodeError, KeyError, ValueError) as e:
                    logger.warning(f"Error parsing line {line_num} in {file_path.name}: {e}")
                    continue
        
        logger.info(f"Loaded {len(states)} perception states from {file_path.name}")
        return states
    
    def _load_csv(self, file_path: Path, max_rows: Optional[int] = None) -> List['PerceptionState']:
        """
        Load from the real perception_full_dataset.csv.

        Columns in that file use shorthand names (lat, lon, alt_m, …).
        We map them to PerceptionState fields via _COL_MAP.
        feasibility_score is derived: 1 − fused_cost (higher cost → lower feasibility).
        """
        nrows = max_rows if (max_rows is not None and max_rows > 0) else None
        df = pd.read_csv(file_path, nrows=nrows)
        states = []

        def g(row, key, default=0.0):
            """Get by mapped or original column name, fallback to default."""
            # Try mapped name first, then original
            for k in (key, *[k for k, v in self._COL_MAP.items() if v == key]):
                if k in row.index:
                    val = row[k]
                    return float(val) if pd.notna(val) else default
            return default

        for _, row in df.iterrows():
            # Terrain clearance → feasibility: 1 if clearance > 100 m, scales down
            clearance = g(row, 'terrain_clearance_m', 100.0)
            feasibility = float(np.clip(clearance / 200.0, 0.0, 1.0))

            state = PerceptionState(
                latitude=g(row, 'lat'),
                longitude=g(row, 'lon'),
                altitude_m=g(row, 'alt_m'),
                terrain_risk=g(row, 'terrain_cost'),
                wind_risk=g(row, 'wind_cost'),
                obstacle_risk=g(row, 'obstacle_cost'),
                threat_risk=g(row, 'combined_threat_prob'),
                fused_risk=g(row, 'fused_cost'),
                feasibility_score=feasibility,
                elevation_m=g(row, 'elev_m'),
                wind_speed_mps=g(row, 'wind_speed_mps'),
                wind_direction_deg=g(row, 'wind_dir_deg'),
                temperature_c=15.0,   # ISA default; not in dataset
            )
            states.append(state)

        logger.info(f"Loaded {len(states):,} perception states from {file_path.name}")
        return states


