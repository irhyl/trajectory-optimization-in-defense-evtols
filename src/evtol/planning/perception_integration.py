def verify_perception_planning_integration() -> bool:
    """
    Verify perception-planning integration is functional.
    
    Returns:
        True if integration works, False otherwise
    """
    try:
        bridge = PerceptionPlanningBridge()
        
        # Check if datasets available
        if not bridge.loader.available_scales:
            logger.error("No perception datasets found")
            return False
        
        # Load a small sample (1000 rows) regardless of total dataset size
        min_scale = min(bridge.loader.available_scales.keys())
        sample_scale = min(1000, min_scale)
        bridge.load_dataset(sample_scale)
        
        # Get first state and constraints
        state, constraints = bridge.get_state_and_constraints(0)
        
        # Verify validity
        assert isinstance(state, dict), "State must be dict"
        assert isinstance(constraints, PlanningConstraintSet), "Constraints must be PlanningConstraintSet"
        assert state['latitude'] != 0 or state['longitude'] != 0, "State must have valid coordinates"
        assert 0 <= constraints.risk_budget <= 1, "Risk budget must be [0,1]"
        
        logger.info("✓ Perception-Planning integration verified")
        return True
    
    except Exception as e:
        logger.error(f"✗ Integration verification failed: {e}")
        return False


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    success = verify_perception_planning_integration()
    exit(0 if success else 1)
