# STATE MAPPER: PERCEPTION → PLANNING
# =============================================================================

class PerceptionToPlanningMapper:
    """
    Maps perception observations to planning state and constraints.
    
    Responsibilities:
        1. Validate perception data
        2. Generate planning constraints
        3. Compute feasible state regions
        4. Flag planning hazards/opportunities
    """
    
    def __init__(self):
        self.name = "PerceptionToPlanningMapper"
    
    def map_state(
        self,
        perception: PerceptionState,
    ) -> Tuple[Dict[str, float], PlanningConstraintSet]:
        """
        Map perception state to planning-compatible representation.
        
        Args:
            perception: PerceptionState with risk/feasibility scores
        
        Returns:
            (state_dict, constraints) for planning
        """
        # State representation for planning
        state = {
            'latitude': perception.latitude,
            'longitude': perception.longitude,
            'altitude_m': perception.altitude_m,
            'elevation_m': perception.elevation_m,
            'wind_speed_mps': perception.wind_speed_mps,
            'wind_direction_deg': perception.wind_direction_deg,
            'temperature_c': perception.temperature_c,
        }
        
        # Constraint generation from perception risks
        constraints = PlanningConstraintSet(
            risk_budget=1.0 - perception.fused_risk,  # Risk tolerance = 1 - observed risk
            energy_budget=1.0 + (perception.wind_speed_mps / 15.0),  # Energy scales with wind
            min_clearance_m=75.0 + (perception.obstacle_risk * 150.0),  # Risk → more clearance
            threat_priority=perception.threat_risk,  # Direct scaling
            environmental_headwind_mps=perception.wind_speed_mps * np.cos(
                np.radians(perception.wind_direction_deg)
            ),
            environmental_crosswind_mps=perception.wind_speed_mps * np.sin(
                np.radians(perception.wind_direction_deg)
            ),
        )
        
        return state, constraints
    
    def rank_feasibility(
        self,
        states: List[PerceptionState],
    ) -> List[Tuple[PerceptionState, float]]:
        """
        Rank perception states by planning feasibility.
        
        Args:
            states: List of perception observations
        
        Returns:
            List of (state, feasibility_score) sorted high→low
        """
        ranked = []
        
        for state in states:
            # Composite feasibility = feasibility × (1 - risk)
            feasibility = state.feasibility_score * (1.0 - state.fused_risk)
            ranked.append((state, feasibility))
        
        ranked.sort(key=lambda x: x[1], reverse=True)
        return ranked


